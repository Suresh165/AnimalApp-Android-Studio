package com.suresh.animalapponline;

public class Animal {
   private String name;
   private int image;

    public Animal(String name, int image){
        this.image = image;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
